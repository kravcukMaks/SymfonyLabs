public function search(?string $search): \Doctrine\ORM\QueryBuilder
{
    $qb = $this->createQueryBuilder('b');

    if ($search) {
        $qb->andWhere('LOWER(b.title) LIKE :s')
           ->setParameter('s', '%' . strtolower($search) . '%');
    }

    return $qb;
}

